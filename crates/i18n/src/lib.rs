pub fn resolve_locale(preference: LocalePreference) -> Locale {
    match preference {
        LocalePreference::En => Locale::En,
        LocalePreference::ZhHans => Locale::ZhHans,
        LocalePreference::System => sys_locale::get_locale()
            .map(|l| {
                if l.starts_with("zh") {
                    Locale::ZhHans
                } else {
                    Locale::En
                }
            })
            .unwrap_or(Locale::En),
    }
}

